"""
Script to assemble sequential images into a video with timestamp-based or fixed frame rate timing.
Uses FFmpeg as the primary encoder (high-quality) with OpenCV as a fallback.
Configuration managed via Hydra.

Two timing modes:
  - enable_fixed_rate: True  → all frames get equal duration 1/video_fixed_rate (default 10 Hz)
  - enable_fixed_rate: False → each frame uses its actual duration from timestamps
    Supports both timestamp formats:
      1. Simple:    {"timestamp": "seconds_nanoseconds"} (generated by video-to-image extraction)
      2. ROS-style: {"image_left_stamp": {"sec": N, "nsec": N}} (from dVRK data collection)
"""

from dataclasses import dataclass
from typing import Union, List, Optional
import hydra
from hydra.core.config_store import ConfigStore
from pathlib import Path
import numpy as np
import cv2
from tqdm import tqdm
import os
import json
import subprocess
import tempfile
import shutil
import time

from dvrk_data_processing.utils.hydra_config import ImageToVideoConfig, PathConfig
from dvrk_data_processing.utils.utility import glob_sorted_frame, create_folder


# ---------------------------------------------------------------------------
# Configuration
# ---------------------------------------------------------------------------

@dataclass
class AppCfg:
    path_config: PathConfig
    preprocess: ImageToVideoConfig
    video_name: str
    workspace: Union[str, Path]
    image_path: Union[str, Path]
    timestamp_path: Union[str, Path]
    save_folder: Union[str, Path]


