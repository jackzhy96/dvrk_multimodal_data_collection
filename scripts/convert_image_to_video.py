# Helper utilities
# ---------------------------------------------------------------------------

def _check_ffmpeg_available() -> bool:
    """Return True if the ffmpeg binary is on PATH and executable."""
    return shutil.which("ffmpeg") is not None


def load_timestamps(timestamp_path: Path) -> np.ndarray:
    """
    Load timestamps from a directory of per-frame JSON files.

    Supports two JSON layouts:
      1. Simple format (generated by video-to-image extraction):
         {"timestamp": "seconds_nanoseconds"}
      2. ROS-style format (from dVRK data collection time_syn/ folders):
         {"image_left_stamp": {"sec": <int>, "nsec": <int>}, ...}

    Args:
        timestamp_path: Path to directory containing numbered JSON files.

    Returns:
        1-D numpy array of timestamps in seconds (sorted by frame index).
    """
    if not timestamp_path.is_dir():
        raise ValueError(f"Timestamp path must be a directory: {timestamp_path}")

    # Get all JSON files sorted numerically by stem (0.json, 1.json, ...)
    json_files = glob_sorted_frame(timestamp_path)

    timestamps: List[float] = []
    for json_file in json_files:
        try:
            with open(json_file, 'r') as f:
                data = json.load(f)

            ts = _parse_single_timestamp(data, json_file)
            if ts is not None:
                timestamps.append(ts)

        except (json.JSONDecodeError, ValueError, KeyError) as e:
            print(f"Warning: Could not parse timestamp from {json_file}: {e}")
            continue

    return np.array(timestamps)


def _parse_single_timestamp(data: dict, source_file: Path) -> Optional[float]:
    """
    Extract a single timestamp (seconds) from one JSON dict.

    Tries formats in order:
      1. ROS-style: image_left_stamp.sec / .nsec
      2. Simple:    timestamp field as "sec_nsec" string
    """
    # --- Format 1: ROS-style with image_left_stamp ---
    if "image_left_stamp" in data:
        stamp = data["image_left_stamp"]
        sec = int(stamp["sec"])
        nsec = int(stamp["nsec"])
        return sec + nsec / 1e9

    # --- Format 2: Simple "seconds_nanoseconds" string ---
    if "timestamp" in data:
        timestamp_str = str(data["timestamp"])
        if '_' in timestamp_str:
            seconds_str, nanoseconds_str = timestamp_str.split('_')
            return float(seconds_str) + float(nanoseconds_str) / 1e9
        else:
            # Fallback: treat as a plain float
            return float(timestamp_str)

    print(f"Warning: Unrecognised timestamp format in {source_file}")
    return None


def calculate_frame_delays(timestamps: np.ndarray, fallback_fps: float) -> np.ndarray:
    """
    Compute per-frame durations (seconds) from an array of absolute timestamps.

    The first frame gets the same duration as the second (no prior reference).
    Any non-positive durations are clamped to 1 µs.

    Args:
        timestamps:   Absolute timestamps in seconds (length N).
        fallback_fps: Used only when there is a single frame.

    Returns:
        1-D array of durations (length N).
    """
    if len(timestamps) < 2:
        return np.array([1.0 / fallback_fps])

    # Consecutive differences give the duration each frame was displayed
    delays = np.diff(timestamps)
    delays = np.maximum(delays, 1e-6)  # clamp to at least 1 µs

    # First frame gets the same duration as the second frame
    return np.concatenate([[delays[0]], delays])


