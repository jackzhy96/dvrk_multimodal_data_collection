def create_video_from_images(config: AppCfg) -> None:
    """
    Create a video from sequential images.

    Tries FFmpeg first for high-quality encoding.  Falls back to OpenCV if
    FFmpeg is not installed or the encoding fails.

    Args:
        config: Hydra application config.
    """
    image_path = Path(config.image_path)
    output_path = Path(config.save_folder)

    # --- Validate input ---
    if not image_path.exists():
        raise FileNotFoundError(f"Image directory not found: {image_path}")
    if not image_path.is_dir():
        raise ValueError(f"Image path is not a directory: {image_path}")

    # Ensure output directory exists
    if not output_path.exists():
        create_folder(output_path)

    # --- Gather and slice image files ---
    image_files = glob_sorted_frame(image_path)
    print(f"Found {len(image_files)} total images")

    start_idx = 0 if config.preprocess.start_frame == -1 else config.preprocess.start_frame
    end_idx = len(image_files) if config.preprocess.end_frame == -1 else config.preprocess.end_frame + 1

    # Clamp to valid range
    start_idx = max(0, min(start_idx, len(image_files) - 1))
    end_idx = max(start_idx + 1, min(end_idx, len(image_files)))
    image_files = image_files[start_idx:end_idx]
    print(f"Processing frames {start_idx} to {end_idx - 1} ({len(image_files)} images)")

    if not image_files:
        raise ValueError("No image files found after applying frame range")

    # --- Determine output dimensions ---
    first_image = cv2.imread(str(image_files[0]))
    if first_image is None:
        raise ValueError(f"Could not read first image: {image_files[0]}")
    original_height, original_width = first_image.shape[:2]

    if config.preprocess.resize_config.enable_resize:
        width, height = config.preprocess.resize_config.new_size
    else:
        width, height = original_width, original_height

    # --- Load timestamps if using variable-rate timing ---
    frame_durations = None  # None signals fixed-rate to the encoders
    if not config.preprocess.enable_fixed_rate:
        if config.timestamp_path is None:
            raise ValueError("timestamp_path is required when enable_fixed_rate is False")

        timestamp_path = Path(config.timestamp_path)
        if not timestamp_path.exists():
            raise FileNotFoundError(f"Timestamp path not found: {timestamp_path}")

        timestamps = load_timestamps(timestamp_path)
        print(f"Loaded {len(timestamps)} total timestamps")

        # Apply the same frame range to timestamps
        ts_start = max(0, min(start_idx, len(timestamps) - 1))
        ts_end = max(ts_start + 1, min(end_idx, len(timestamps)))
        timestamps = timestamps[ts_start:ts_end]
        print(f"Using timestamps {ts_start} to {ts_end - 1} ({len(timestamps)} timestamps)")

        if len(timestamps) != len(image_files):
            raise ValueError(
                f"Number of timestamps ({len(timestamps)}) doesn't match "
                f"number of images ({len(image_files)})"
            )

        frame_durations = calculate_frame_delays(timestamps, config.preprocess.video_fixed_rate)

        total_dur = timestamps[-1] - timestamps[0] if len(timestamps) > 1 else 1.0
        avg_fps = len(image_files) / total_dur if total_dur > 0 else config.preprocess.video_fixed_rate
        print(f"Timestamp-based timing  |  total duration: {total_dur:.2f}s  |  avg fps: {avg_fps:.2f}")
    else:
        print(f"Fixed frame rate: {config.preprocess.video_fixed_rate} fps")

    # --- Encode ---
    output_file = output_path / f"{config.video_name}.mp4"

    use_ffmpeg = _check_ffmpeg_available()
    success = False
    t_start = time.time()

    if use_ffmpeg:
        print(f"Encoding with FFmpeg ({config.preprocess.ffmpeg_config.codec}, "
              f"crf={config.preprocess.ffmpeg_config.crf}, "
              f"preset={config.preprocess.ffmpeg_config.preset})")
        success = _create_video_ffmpeg(
            image_files, output_file, width, height, config, frame_durations,
        )
        if not success:
            print("FFmpeg encoding failed — falling back to OpenCV")
    else:
        print("FFmpeg not found on PATH — using OpenCV fallback")

    if not success:
        print("Encoding with OpenCV (mp4v codec)")
        success = _create_video_opencv(
            image_files, output_file, width, height, config, frame_durations,
        )

    elapsed = time.time() - t_start
    if success:
        # Show file size alongside the path
        size_mb = output_file.stat().st_size / (1024 * 1024)
        print(f"Video saved to: {output_file}  ({size_mb:.1f} MB)")
        print(f"Encoding completed in {elapsed:.1f}s  "
              f"({len(image_files)} frames, {len(image_files)/elapsed:.1f} frames/s)")
    else:
        print(f"Error: Failed to create video at {output_file}  (elapsed {elapsed:.1f}s)")


# ---------------------------------------------------------------------------
# Hydra entry point
# ---------------------------------------------------------------------------

cs = ConfigStore.instance()
cs.store(name="image_to_video", node=AppCfg)

# Set config path
p_config = Path.cwd().parents[0] / 'config'


@hydra.main(
    version_base=None,
    config_path=str(p_config),
    config_name="config_i2v_jack"
)
def main(cfg: AppCfg) -> None:
    """
    Main function to convert images to video using hydra configuration.

    Args:
        cfg: ImageToVideoConfig object
    """
    # Create video
    create_video_from_images(cfg)


if __name__ == '__main__':
    main()
    print('Image to video conversion completed!')
