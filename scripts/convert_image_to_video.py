# FFmpeg-based encoding (primary)
# ---------------------------------------------------------------------------

def _parse_ffmpeg_progress(line: str) -> Optional[int]:
    """
    Extract the current frame number from an FFmpeg stderr progress line.

    FFmpeg periodically prints lines like:
      frame=  100 fps= 25 q=28.0 size=  256kB time=00:00:04.00 ...
    Returns the frame number if found, else None.
    """
    import re
    m = re.search(r'frame=\s*(\d+)', line)
    return int(m.group(1)) if m else None


def _create_video_ffmpeg(
    image_files: List[Path],
    output_file: Path,
    width: int,
    height: int,
    config: "AppCfg",
    frame_durations: Optional[np.ndarray] = None,
) -> bool:
    """
    Encode images into a video using FFmpeg's concat demuxer.

    For fixed-rate mode, every frame gets duration 1/fps.
    For timestamp mode, each frame gets its actual duration from *frame_durations*.
    Shows a real-time progress bar by streaming FFmpeg's stderr.

    Args:
        image_files:     Sorted list of image paths.
        output_file:     Destination .mp4 path.
        width, height:   Target resolution (used for resize filter if needed).
        config:          Full application config.
        frame_durations: Per-frame durations in seconds (None → fixed rate).

    Returns:
        True on success, False on failure.
    """
    ffmpeg_cfg = config.preprocess.ffmpeg_config
    fps = config.preprocess.video_fixed_rate
    need_resize = config.preprocess.resize_config.enable_resize
    total_frames = len(image_files)

    # --- Build the concat demuxer text file ---
    tmp_fd, tmp_path = tempfile.mkstemp(suffix=".txt", prefix="ffmpeg_concat_")
    temp_list_file = Path(tmp_path)

    try:
        with os.fdopen(tmp_fd, 'w') as f:
            for idx, img_file in enumerate(image_files):
                f.write(f"file '{img_file.absolute()}'\n")
                if frame_durations is not None:
                    # Timestamp mode: per-frame duration
                    f.write(f"duration {frame_durations[idx]:.9f}\n")
                else:
                    # Fixed rate mode: uniform duration
                    f.write(f"duration {1.0 / fps:.9f}\n")
            # FFmpeg concat demuxer requires the last file repeated without duration
            if image_files:
                f.write(f"file '{image_files[-1].absolute()}'\n")

        # --- Assemble the ffmpeg command ---
        cmd = [
            "ffmpeg",
            "-y",                           # overwrite output
            "-f", "concat",
            "-safe", "0",
            "-i", str(temp_list_file),
        ]

        # Optional resize via scale filter
        if need_resize:
            cmd += ["-vf", f"scale={width}:{height}"]

        cmd += [
            "-c:v", ffmpeg_cfg.codec,
            "-crf", str(ffmpeg_cfg.crf),
            "-preset", ffmpeg_cfg.preset,
            "-pix_fmt", ffmpeg_cfg.pixel_format,
            "-r", str(fps),                 # output frame rate
            str(output_file),
        ]

        # --- Run FFmpeg with real-time progress tracking ---
        # Stream stderr so we can parse "frame=..." lines for the progress bar
        proc = subprocess.Popen(
            cmd,
            stdout=subprocess.PIPE,
            stderr=subprocess.PIPE,
        )

        pbar = tqdm(total=total_frames, desc="[FFmpeg] Encoding", unit="frame")
        stderr_lines: List[str] = []  # accumulate for error reporting

        # FFmpeg writes progress to stderr; read character-by-character
        # because lines are terminated by \r (carriage return) not \n
        buf = ""
        while True:
            ch = proc.stderr.read(1)
            if not ch:
                break
            ch = ch.decode("utf-8", errors="replace")

            if ch in ('\r', '\n'):
                stderr_lines.append(buf)
                frame_num = _parse_ffmpeg_progress(buf)
                if frame_num is not None:
                    # Update bar to the absolute frame position
                    pbar.n = min(frame_num, total_frames)
                    pbar.refresh()
                buf = ""
            else:
                buf += ch

        proc.wait()
        pbar.n = total_frames
        pbar.refresh()
        pbar.close()

        if proc.returncode != 0:
            print(f"Warning: FFmpeg encoding failed (rc={proc.returncode}):")
            # Print last few stderr lines for diagnosis
            for line in stderr_lines[-10:]:
                if line.strip():
                    print(f"  {line.strip()}")
            return False

        return True

    except FileNotFoundError:
        print("Warning: FFmpeg binary not found")
        return False

    finally:
        # Always clean up the temporary concat list
        if temp_list_file.exists():
            temp_list_file.unlink()


# ---------------------------------------------------------------------------
# OpenCV-based encoding (fallback)
# ---------------------------------------------------------------------------

def _create_video_opencv(
    image_files: List[Path],
    output_file: Path,
    width: int,
    height: int,
    config: "AppCfg",
    frame_durations: Optional[np.ndarray] = None,
) -> bool:
    """
    Fallback encoder using cv2.VideoWriter.

    For timestamp mode the only option is to repeat frames proportionally
    to approximate the real timing (VideoWriter has a fixed fps).

    Args:
        image_files:     Sorted list of image paths.
        output_file:     Destination .mp4 path.
        width, height:   Target resolution.
        config:          Full application config.
        frame_durations: Per-frame durations in seconds (None → fixed rate).

    Returns:
        True on success, False on failure.
    """
    fps = config.preprocess.video_fixed_rate
    need_resize = config.preprocess.resize_config.enable_resize

    # Use mp4v codec for the OpenCV fallback (widely supported)
    fourcc = cv2.VideoWriter_fourcc(*"mp4v")
    writer = cv2.VideoWriter(str(output_file), fourcc, fps, (width, height))

    if not writer.isOpened():
        print(f"Warning: OpenCV VideoWriter failed to open {output_file}")
        return False

    for idx, image_file in enumerate(tqdm(image_files, desc="[OpenCV] Encoding", unit="frame")):
        image = cv2.imread(str(image_file))
        if image is None:
            print(f"Warning: Could not read image {image_file}, skipping...")
            continue

        if need_resize:
            image = cv2.resize(image, (width, height))

        if frame_durations is not None:
            # Approximate variable timing by repeating frames
            repeat_count = max(1, int(frame_durations[idx] * fps))
            for _ in range(repeat_count):
                writer.write(image)
        else:
            writer.write(image)

    writer.release()
    return True


